CREATE VIEW [dbo].[_Fluig_ITENS_COMPRA]

AS

-- DEFININDO UM ITEM DE ALMOXARIFADO
SELECT 1 AS CODFILIAL, TPRODUTO.IDPRD, TPRODUTO.CODIGOPRD, 
TPRODUTO.NOMEFANTASIA, TPRODUTO.CODIGOPRD+ ' - '  +TPRODUTO.NOMEFANTASIA as NOME, TPRODUTO.DESCRICAO
	FROM TPRODUTO 
		JOIN TPRODUTODEF	ON ((TPRODUTO.IDPRD=TPRODUTODEF.IDPRD))
WHERE	INATIVO			<>	1
AND		ULTIMONIVEL		=	1
AND		TPRODUTODEF.CODTB1FAT = '02' /* Produto Somente Compras - DF */

UNION ALL

SELECT 2 AS CODFILIAL, TPRODUTO.IDPRD, TPRODUTO.CODIGOPRD, 
TPRODUTO.NOMEFANTASIA, TPRODUTO.CODIGOPRD+ ' - '  +TPRODUTO.NOMEFANTASIA as NOME, TPRODUTO.DESCRICAO
	FROM TPRODUTO 
		JOIN TPRODUTODEF	ON ((TPRODUTO.IDPRD=TPRODUTODEF.IDPRD))
WHERE	INATIVO			<>	1
AND		ULTIMONIVEL		=	1
AND		TPRODUTODEF.CODTB2FAT = '02' /* Produto Somente Compras - RJ */


GO

